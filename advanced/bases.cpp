#include <iostream>

using namespace std;

int main () {
    int decimal = 42;
    cout << "Decimal: " << decimal << endl;

    int octal = 052;
    cout << "Octal: " << octal << endl;

    int hex = 0x2a;
    cout << "Hex: " << hex << endl;

    int bin = 0b101010;
    cout << "Binary: " << bin << endl;
}